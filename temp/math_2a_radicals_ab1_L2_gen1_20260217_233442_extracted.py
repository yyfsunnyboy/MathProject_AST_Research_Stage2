def simplify_radical(n):
    if n == 0:
        return (0, 0)
    # 找到最大的平方因子
    sqrt_n = int(math.isqrt(n))
    for i in range(sqrt_n, 0, -1):
        if n % (i*i) == 0:
            coeff = i
            radicand = n // (i*i)
            return (coeff, radicand)
    return (1, n)

