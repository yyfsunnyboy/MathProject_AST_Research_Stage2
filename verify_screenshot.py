# 验证用户截图中的题目
x = 2
f_x = (x - 2) * (-x - 2)**2
print('验证点是否在曲线上:')
print(f'f(x) = (x - 2)(-x - 2)^2')
print(f'f(2) = (2 - 2) × (-2 - 2)^2')
print(f'f(2) = 0 × 16 = {f_x}')
print(f'点 P(2, 0) 是否在曲线上: {f_x == 0}')
print()

# 检查导数
# f(x) = (x-2)(-x-2)^2
# u = x-2, u' = 1
# v = (-x-2)^2, v' = 2(-x-2)(-1) = -2(-x-2)
# f'(x) = 1*(-x-2)^2 + (x-2)*(-2)(-x-2)
# f'(x) = (-x-2)^2 - 2(x-2)(-x-2)
# f'(x) = (-x-2)[(-x-2) - 2(x-2)]
# f'(x) = (-x-2)[(-x-2) - 2x + 4]
# f'(x) = (-x-2)[-x - 2 - 2x + 4]
# f'(x) = (-x-2)[-3x + 2]

x = 2
f_prime = (-x - 2) * (-3*x + 2)
print(f'导数 f\'(x) = (-x-2)(-3x+2)')
print(f'f\'(2) = (-2-2)(-6+2)')
print(f'f\'(2) = (-4)(-4) = {f_prime}')
print()

# 切线方程：y - y0 = m(x - x0)
# y - 0 = 16(x - 2)
# y = 16x - 32
print(f'切线方程: y = {f_prime}x + {-f_prime * 2}')
