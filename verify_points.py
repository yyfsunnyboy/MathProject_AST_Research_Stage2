# 题目 1: f(x) = (x + 0)(-2x + 1)^2, 点 P((-2), (-50))
x = -2
f_x = (x + 0) * (-2*x + 1)**2
print('题目 1: f(x) = (x + 0)(-2x + 1)^2, 点 P(-2, -50)')
print(f'  f(-2) = (-2 + 0) × (-2×(-2) + 1)^2')
print(f'  f(-2) = (-2) × (4 + 1)^2')
print(f'  f(-2) = (-2) × 5^2')
print(f'  f(-2) = (-2) × 25 = {f_x}')
print(f'  期望值: -50')
if f_x == -50:
    print('  验证: ✓')
else:
    print(f'  验证: ✗ 错误! {f_x} ≠ -50')
print()

